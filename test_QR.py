import QR


def test_unstable_gram_schmidt():
    matrix1 = [[1,0,1], [2,1,0]]
    matrix2 = [[4,3],[5,6]]
    assert QR.unstable_Gram_Schmidt(matrix1) == [[[0.7071067811865475, 0.0, 0.7071067811865475], [(0.577350269189626+0j), (0.5773502691896258+0j), (-0.5773502691896257+0j)]], [[1.4142135623730951, 0], [(1.414213562373095+0j), (1.7320508075688772+0j)]]]
    assert QR.unstable_Gram_Schmidt(matrix2) == [[[0.8, 0.6000000000000001], [(-0.6000000000000008+0j), (0.7999999999999996+0j)]], [[5.0, 0], [(7.6000000000000005+0j), (1.7999999999999994+0j)]]]




def test_stable_gram_schmidt():
    matrix1 = [[1, 0, 1], [2, 1, 0]]
    matrix2 = [[4,3],[5,6]]
    assert QR.stable_Gram_Schmidt(matrix1) == [[[0.7071067811865475, 0.0, 0.7071067811865475], [(0.577350269189626+0j), (0.5773502691896258+0j), (-0.5773502691896257+0j)]], [[1.4142135623730951, (1.414213562373095+0j)], [0, (1.7320508075688772+0j)]]]
    assert QR.stable_Gram_Schmidt(matrix2) == [[[0.8, 0.6000000000000001], [(-0.6000000000000008+0j), (0.7999999999999996+0j)]], [[5.0, (7.6000000000000005+0j)], [0, (1.7999999999999994+0j)]]]


